#!/usr/bin/env node
// ════════════════════════════════════════════════════════════════════════
// stripe-brand-sheet.mjs — emit the Stripe Dashboard values-sheet
// ════════════════════════════════════════════════════════════════════════
//
// Stripe Hosted Checkout is the one customer touchpoint that can't auto-consume
// the brand. Its colours and logo live in the Stripe DASHBOARD (Settings →
// Branding), set by hand. So this surface needs a SHEET: the exact resolved
// values to type in, sourced — never guessed — from brand-values.js.
//
// This script imports getBrandValues() (the same emit layer the emails and the
// download page read) and writes the values Stripe asks for into a committed
// markdown file, docs/brand/stripe-sync.md, with a sync checklist. The file is
// the readable artifact; this script is the source. When the accent changes in
// petals-brand, re-run this → the sheet updates → apply the checklist in Stripe.
//
//   run:  node stripe-brand-sheet.mjs           (writes the sheet)
//         node stripe-brand-sheet.mjs --check    (exits 1 if a re-run would change it)
//
// Stripe Hosted Checkout renders on a light page, so we read the TRANSACTIONAL
// surface — the same cream receipt palette the post-sale emails use. The accent
// is the one brand purple on every surface.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

import { getBrandValues } from './brand-values.js';

const HERE = dirname(fileURLToPath(import.meta.url));
const SHEET = join(HERE, '..', '..', 'docs', 'brand', 'stripe-sync.md');

// ── The mark assets Stripe needs uploaded. Stripe wants raster (PNG/JPG),
// min 128×128 for the logo. These are the pre-rendered brand marks the site
// already ships; pointing Stripe at them keeps the dashboard on the same mark.
const LOGO_ASSET = 'website/public/marks/petals-mark-180.png';  // 180×180, clears Stripe's 128px floor
const ICON_ASSET = 'website/public/marks/mark-email.png';       // 120×120 square glyph

function buildSheet() {
  const b = getBrandValues('transactional');
  const today = new Date().toISOString().slice(0, 10);

  return `<!--
  GENERATED by branding/tokens/stripe-brand-sheet.mjs — DO NOT EDIT BY HAND.
  Source of truth: branding/tokens/brand-values.js (getBrandValues('transactional')).
  Re-run \`node branding/tokens/stripe-brand-sheet.mjs\` after any brand-value change.
-->

# Stripe branding sync

Stripe Hosted Checkout is the one customer touchpoint that can't read the brand
tokens. Its accent and logo are set by hand in the Stripe Dashboard. This sheet
holds the exact values to type in. They're emitted from \`brand-values.js\`, the
same emit layer the receipt emails and the download page read. Don't guess these.

Surface read: \`${b.surface}\` (cream receipt palette — Stripe checkout is a light page).

## Values

| Stripe field | Value |
|---|---|
| Brand / accent colour | \`${b.colors.accent}\` |
| Accent text (reads on accent) | \`${b.colors.accentText}\` |
| Logo (upload) | \`${LOGO_ASSET}\` |
| Icon (upload) | \`${ICON_ASSET}\` |

## Sync checklist

Stripe Dashboard → Settings → Branding:

- [ ] Brand colour = \`${b.colors.accent}\`
- [ ] Accent colour = \`${b.colors.accent}\`
- [ ] Upload logo = \`${LOGO_ASSET}\`
- [ ] Upload icon = \`${ICON_ASSET}\`
- [ ] Verify in TEST mode (open a test checkout, confirm purple + mark render)
- [ ] Stamp the sync date below and commit this file

## Last sync

- Sheet generated: ${today}
- Applied to Stripe Dashboard: _not yet — stamp the date here after applying_
- Commit:  _add the commit ref after applying_
`;
}

const next = buildSheet();

if (process.argv.includes('--check')) {
  let current = '';
  try { current = readFileSync(SHEET, 'utf8'); } catch { /* not generated yet */ }
  // Ignore the generated-date line so --check tracks brand-value drift, not the clock.
  const strip = (s) => s.replace(/^- Sheet generated: .*$/m, '');
  if (strip(next) !== strip(current)) {
    console.error('stripe-brand-sheet: OUT OF DATE — re-run `node stripe-brand-sheet.mjs`.');
    process.exit(1);
  }
  console.log('stripe-brand-sheet: up to date.');
} else {
  writeFileSync(SHEET, next);
  console.log(`stripe-brand-sheet: wrote ${SHEET}`);
}
