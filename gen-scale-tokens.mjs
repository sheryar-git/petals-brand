#!/usr/bin/env node
// ════════════════════════════════════════════════════════════════════════
// gen-scale-tokens.mjs — build-time codegen for the SITE scale tokens
// ════════════════════════════════════════════════════════════════════════
//
// Rumi's architecture (site redesign, Phase 1a): the relational scale fns are
// the SOURCE; petals-tokens.css stays the shippable artifact. This script runs
// the scale fns and EMITS a generated `:root` block of site-scale tokens into
// petals-tokens.css, between fixed markers. CSS ships; scales drive.
//
//   source  → typeScale() · spacingScale() · weights/line-height/letter-spacing
//             (branding/tokens/typeScale.js · spacingScale.js)
//   target  → the BEGIN/END GENERATED block in petals-tokens.css
//
// ONE emitter, THREE tier-inputs (type · spacing · weight) — never hand-maintain
// one tier while generating another. Idempotent: re-running reproduces the exact
// same block (no diff on a second run). It ONLY rewrites the marked block; every
// byte outside the markers — every synth token — is left untouched.
//
//   run:  node gen-scale-tokens.mjs          (writes the block)
//         node gen-scale-tokens.mjs --check   (exits 1 if a re-run would change it)

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

import {
  typeScale,
  SITE_LINE_HEIGHT,
  SITE_LETTER_SPACING,
  SITE_WEIGHTS,
} from './typeScale.js';
import { spacingScale } from './spacingScale.js';

const HERE = dirname(fileURLToPath(import.meta.url));
const CSS = join(HERE, 'petals-tokens.css');

const BEGIN = '/* === BEGIN GENERATED: site scales === */';
const END = '/* === END GENERATED: site scales === */';

// ── format helpers ───────────────────────────────────────────────────────
// Pad every `--token:` to a common column so the block reads as aligned
// monospace — the same column discipline as the hand-written token blocks.
function emitGroup(title, entries) {
  const width = Math.max(...entries.map(([k]) => k.length));
  const lines = entries.map(
    ([k, v]) => `  ${(k + ':').padEnd(width + 1)} ${v};`,
  );
  return [`  /* ${title} */`, ...lines].join('\n');
}

// Line-height / letter-spacing are keyed by bare tier name → prefix here so
// the type, lh, and ls groups all read off one tier vocabulary.
function tierEntries(prefix, map) {
  return Object.entries(map).map(([tier, v]) => [`${prefix}${tier}`, v]);
}

// ── build the generated block ─────────────────────────────────────────────
function buildBlock() {
  const groups = [
    emitGroup('type ladder — typeScale(base=16, ratio=√φ=1.272)', Object.entries(typeScale())),
    emitGroup('line-height — relational, tightens as size grows', tierEntries('--lh-site-', SITE_LINE_HEIGHT)),
    emitGroup('letter-spacing — relational, tighter as size grows', tierEntries('--ls-site-', SITE_LETTER_SPACING)),
    emitGroup('weight — variable Berkeley Mono, three weights max', Object.entries(SITE_WEIGHTS).map(([k, v]) => [k, String(v)])),
    emitGroup('spacing — spacingScale(5), Fibonacci on the 5px atom', Object.entries(spacingScale())),
  ];

  return [
    BEGIN,
    '/*',
    ' * SITE scale tokens — DO NOT EDIT BY HAND. Generated by gen-scale-tokens.mjs.',
    ' * Source: typeScale.js / spacingScale.js. Edit the scales, then re-run the script.',
    ' * Namespaced `*-site-*` so they never collide with the instrument UI tokens',
    ' * (the synth --fs-label/value/secondary/header, --sp-1..6 stay untouched).',
    ' */',
    ':root {',
    '',
    groups.join('\n\n'),
    '',
    '}',
    END,
  ].join('\n');
}

// ── splice into petals-tokens.css ──────────────────────────────────────────
// Replace an existing marked block in place; else append at EOF. Either path
// touches ONLY the marked region.
function splice(css, block) {
  const b = css.indexOf(BEGIN);
  const e = css.indexOf(END);
  if (b !== -1 && e !== -1) {
    return css.slice(0, b) + block + css.slice(e + END.length);
  }
  // first run: append, preserving the file's single trailing newline.
  const base = css.endsWith('\n') ? css.slice(0, -1) : css;
  return `${base}\n\n${block}\n`;
}

const css = readFileSync(CSS, 'utf8');
const next = splice(css, buildBlock());

if (process.argv.includes('--check')) {
  if (next !== css) {
    console.error('gen-scale-tokens: OUT OF DATE — re-run `node gen-scale-tokens.mjs`.');
    process.exit(1);
  }
  console.log('gen-scale-tokens: up to date.');
} else if (next !== css) {
  writeFileSync(CSS, next);
  console.log('gen-scale-tokens: wrote generated block to petals-tokens.css');
} else {
  console.log('gen-scale-tokens: no change (already up to date).');
}
